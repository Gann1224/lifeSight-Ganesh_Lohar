# 2. Current budget — with an overlapping DEPRECATED twin (ambiguity trap)
# =========================================================================== #
def get_current_budget(mmmRequestId: str, is_additive_model: str = "false") -> dict[str, Any]:
    """Real: GET /v2/planner/current-budget. CANONICAL current-budget tool.

    Param MUST be camelCase `mmmRequestId` (snake_case is a different signature and
    will error). Response includes a large `referencePoint` decomposition you should
    slim before showing a model.
    """
    if str(mmmRequestId) not in _MODELS_BY_ID:
        return _err(404, f"model {mmmRequestId} not found")
    ref = {f"period_{p}": {"decompositionData": [
        {"variableName": col, "totalInput": round(_RNG.uniform(0, 5000), 2)} for _, col in _CHANNELS
    ]} for p in range(6)}
    return _ok({
        "startDate": "2025-12-02", "endDate": "2025-12-31",
        "mmmCurrentBudgetResponseList": [
            {"timePeriod": tp, "budget": b} for tp, b in
            [("week", 1_931_871), ("month", 8_279_449), ("quarter", 24_838_348)]
        ],
        "referencePoint": ref,
    })


def get_planner_budget(model_id: str) -> dict[str, Any]:
    """DEPRECATED. Real: GET /planner/budget (v1). Superseded by get_current_budget.

    Looks like it does the same job. It does NOT: snake_case param, a stale/different
    shape, and values that lag reality. Using this instead of get_current_budget is a
    correctness bug. It is here on purpose.
    """
    if str(model_id) not in _MODELS_BY_ID:
        return _err(404, f"model {model_id} not found")
    return _ok({"budget_by_channel": {name: 1234.0 for name, _ in _CHANNELS},
                "_note": "v1 legacy shape; stale; do not use for decisions"})


