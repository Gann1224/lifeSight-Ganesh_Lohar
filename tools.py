def list_models() -> dict[str, Any]:
    """Real: GET /mmm/models. Returns ALL models as full fat objects (~200, big).

    No pagination, no limit. This is the ~19MB blob. Newest is NOT guaranteed first.
    """
    return _ok(list(_MODELS))


def get_model_details(mmmRequestId: str) -> dict[str, Any]:
    """Real: GET /mmm/models/{id}. One full model object.

    NOTE: overlaps heavily with list_models — deciding when to use which (and not
    calling list_models just to read one model's fields) is part of the test.
    Param is camelCase.
    """
    m = _MODELS_BY_ID.get(str(mmmRequestId))
    if not m:
        return _err(404, f"model {mmmRequestId} not found")
    return _ok(dict(m))


def get_mmm_input(mmmRequestId: str) -> dict[str, Any]:
    """Real: GET /mmm/input. current_revenue = Sum(data.kpi[0].values)."""
    if str(mmmRequestId) not in _MODELS_BY_ID:
        return _err(404, f"model {mmmRequestId} not found")
    return _ok({"kpi": [{"name": "revenue", "values": [40000.0, 42000.0, 38000.0, 45000.0]}]})


