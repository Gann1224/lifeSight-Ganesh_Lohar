"""
Internal tools for the budget-optimisation assignment.

These are MOCK versions of real Lifesight platform endpoints. They run fully offline
(no network, no credentials) but faithfully mirror the real API's method, params,
response shapes, and — importantly — its real-world messiness. Each tool's docstring
names the real endpoint it stands in for.

RULES FOR CANDIDATES:
  - Treat these as the ONLY source of truth. No web, no external calls.
  - Do NOT edit this file's behaviour. You may wrap it, cache it, slim its output.
  - Some tools are deliberately messy: huge responses, overlapping tools, strict
    param names, hard ordering dependencies, ambiguous business terms, and required
    inputs the user often omits. Handling that mess reliably is the assignment.

Envelope conventions (mirrors the real `make_api_request`):
  success -> {"status": "success", "data": <payload>}
  error   -> {"status": "error", "error_code": <int>, "error_message": <str>}

A tiny in-process SESSION dict simulates server-side state (like the real agent's
session state). Some tools READ state written by earlier tools — get the order wrong
and you get an error, not a guess.
"""

from __future__ import annotations

import random
from typing import Any

# --------------------------------------------------------------------------- #
# Simulated server-side session state (reset via reset_session()).
# --------------------------------------------------------------------------- #
SESSION: dict[str, Any] = {}


def reset_session() -> None:
    SESSION.clear()


def _ok(data: Any) -> dict[str, Any]:
    return {"status": "success", "data": data}


def _err(code: int, msg: str) -> dict[str, Any]:
    return {"status": "error", "error_code": code, "error_message": msg}


# --------------------------------------------------------------------------- #
# Canned data (deterministic — fixed seed).
# --------------------------------------------------------------------------- #
_RNG = random.Random(42)
_KPIS = ["Revenue", "Conversions", "Installs"]
_COUNTRIES = [("US", "USD"), ("DE", "EUR"), ("MX", "MXN"), ("IN", "INR"), ("GB", "GBP")]
_CHANNELS = [
    ("Google PMAX", "spend_google_pmax"),
    ("Google Branded Search", "spend_google_branded"),
    ("Meta BOF", "spend_meta_bof"),
    ("Meta Prospecting", "spend_meta_prospecting"),
    ("TikTok", "spend_tiktok"),
    ("CTV", "spend_ctv"),
    ("Snapchat", "spend_snap"),
]


def _make_models(n: int = 220) -> list[dict[str, Any]]:
    """~220 full model objects, each fat with junk — mirrors the real ~19MB /mmm/models.

    Only a few fields matter (id, modelName, outcomeKPI, modelStatus, createdAt,
    country, currency). The rest is provenance/plumbing you should slim away.
    """
    models = []
    for i in range(n):
        kpi = _RNG.choice(_KPIS)
        country, currency = _RNG.choice(_COUNTRIES)
        created = 1_700_000_000 + i * 90_000  # increasing => higher i is newer
        models.append({
            "id": 4_000_000_000_000_000 + i * 111_113,
            "modelName": f"model_{i}_{kpi.lower()}",
            "modelDisplayName": None if i % 3 else f"{kpi} Model {i}",
            "outcomeKPI": kpi,
            "modelStatus": "Success" if i % 7 else "Failed",
            "createdAt": {"seconds": created, "nanos": 0},
            "country": country,
            "currency": currency,
            "additiveModel": bool(i % 2),
            "solId": f"sol_{i:04d}",
            # ---- junk below: provenance/plumbing/raw internals (the fat) ----
            "gcsBucketName": "moda-mmm-artifacts-stg",
            "gcsPath": f"gs://moda-mmm-artifacts-stg/models/{i}/robyn_output/",
            "gcsPreviousPath": f"gs://moda-mmm-artifacts-stg/models/{i}/prev/",
            "fileName": f"input_{i}.csv",
            "previousSuccessFileName": f"input_{i}_prev.csv",
            "hyperParametersList": [{"name": f"hp_{k}", "value": _RNG.random()} for k in range(40)],
            "prophetVarsIds": [_RNG.randint(1, 9999) for _ in range(25)],
            "selectedSchema": {"cols": [f"col_{c}" for c in range(30)]},
            "rawTrainingWindow": [round(_RNG.random(), 5) for _ in range(60)],
            "refreshFrequency": _RNG.choice(["weekly", "monthly"]),
            "createdBy": "etl-service@moda", "updatedBy": "etl-service@moda",
            "lastUpdatedAt": {"seconds": created + 3600, "nanos": 0},
            "failedReason": None,
            "predictionAccuracy": round(_RNG.uniform(0.7, 0.95), 4),
        })
    return models


_MODELS = _make_models()
_MODELS_BY_ID = {str(m["id"]): m for m in _MODELS}


def _optimise_rows(total_budget: float, constraint_type: int) -> list[dict[str, Any]]:
    """Per-channel optimise rows in the REAL nested shape + an 'All Platforms' rollup."""
    rows = []
    total_opt_spend, total_opt_rev, total_cur_spend, total_cur_rev = 0.0, 0.0, 0.0, 0.0
    n = len(_CHANNELS)
    for idx, (name, col) in enumerate(_CHANNELS):
        cur_spend = 0.0 if idx == n - 1 else round(total_budget / n * _RNG.uniform(0.6, 1.4), 2)
        # aggressive (3) reallocates more than conservative (1)
        factor = {0: 1.0, 1: 1.05, 2: 1.2, 3: 1.6}.get(constraint_type, 1.05)
        opt_spend = round(cur_spend * _RNG.uniform(0.8, factor), 2)
        cur_rev = round(cur_spend * _RNG.uniform(2.0, 4.0), 2)
        opt_rev = round(opt_spend * _RNG.uniform(2.2, 4.5), 2)
        total_cur_spend += cur_spend; total_opt_spend += opt_spend
        total_cur_rev += cur_rev; total_opt_rev += opt_rev
        rows.append({
            "platformName": name,
            "excelColumnName": col,
            "currentBudgetData": {"spend": cur_spend, "revenue": cur_rev,
                                  "iROAS": round(cur_rev / cur_spend, 2) if cur_spend else 0},
            "optimisedBudgetData": {"spend": opt_spend, "response": opt_rev,
                                    "iROAS": round(opt_rev / opt_spend, 2) if opt_spend else 0},
            "totalOptimisedBudget": opt_spend,
            # raw saturation curve — big, rarely needed by the model
            "simulatedResponseCurveList": [{"spend": s * 1000, "revenue": s * 1000 * _RNG.uniform(2, 4)}
                                           for s in range(200)],
        })
    rows.append({
        "platformName": "All Platforms",
        "currentBudgetData": {"spend": round(total_cur_spend, 2), "revenue": round(total_cur_rev, 2)},
        "optimisedBudgetData": {"spend": round(total_opt_spend, 2), "response": round(total_opt_rev, 2),
                                "iROAS": round(total_opt_rev / total_opt_spend, 2) if total_opt_spend else 0},
        "totalOptimisedBudget": round(total_opt_spend, 2),
    })
    return rows


