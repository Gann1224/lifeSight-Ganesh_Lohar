def run_default_optimise(mmmRequestId: str, timePeriod: str = "quarter") -> dict[str, Any]:
    """Real: POST /v2/planner/budget-optimise (baseline). Establishes the baseline.

    Stage 1. MUST run before run_constrained_optimise. Writes server state.
    """
    if str(mmmRequestId) not in _MODELS_BY_ID:
        return _err(404, f"model {mmmRequestId} not found")
    SESSION["default_done"] = True
    SESSION["default_model"] = str(mmmRequestId)
    SESSION["timePeriod"] = timePeriod
    return _ok({"baseline": "ready", "mmmRequestId": str(mmmRequestId), "timePeriod": timePeriod})


def run_constrained_optimise(mmmRequestId: str, totalBudget: float,
                             constraintType: int) -> dict[str, Any]:
    """Real: POST /v2/planner/budget-optimise (core). Constrained allocation.

    Stage 2. ERRORS unless run_default_optimise ran first for the same model.
    constraintType: 0=Current, 1=Conservative, 2=Moderate, 3=Aggressive.
    totalBudget REQUIRED and must be > 0. Returns the big dateRangeToResponseMap.
    """
    if not SESSION.get("default_done") or SESSION.get("default_model") != str(mmmRequestId):
        return _err(409, "run_default_optimise must succeed for this model before constrained optimise")
    if totalBudget is None or float(totalBudget) <= 0:
        return _err(400, "totalBudget is required and must be > 0")
    if constraintType not in (0, 1, 2, 3):
        return _err(400, "constraintType must be one of 0,1,2,3")
    rows = _optimise_rows(float(totalBudget), int(constraintType))
    result = {"dateRangeToResponseMap": {
        "aggregated_aggregated": {"mmmBudgetOptimisationResponseList": rows},
        "range_0": {"budgetRequestId": 90_001},
    }}
    SESSION["optimisation_result"] = result
    SESSION["last_total_budget"] = float(totalBudget)
    return _ok(result)


def forecast_revenue(mmmRequestId: str) -> dict[str, Any]:
    """Real: POST /v2/planner/forecast-revenue. Forecast from the constrained result.

    ERRORS unless run_constrained_optimise ran first (needs its budgetRequestId + the
    model's solId). Returns weekly forecast series (slim before returning to the LLM).
    """
    if "optimisation_result" not in SESSION:
        return _err(409, "run_constrained_optimise must succeed before forecast_revenue")
    m = _MODELS_BY_ID.get(str(mmmRequestId))
    if not m:
        return _err(404, f"model {mmmRequestId} not found")
    weekly = [{"week": w, "forecast": round(200000 * (1 + w * 0.01), 2)} for w in range(52)]
    return _ok({"totalForecastRevenue": round(sum(x["forecast"] for x in weekly), 2),
                "weekly": weekly, "solId": m["solId"]})


# =========================================================================== #
# 4. Scenarios (multi-scenario compare) + target-KPI math
# =========================================================================== #
def save_scenario(label: str) -> dict[str, Any]:
    """Save the current optimisation_result under a label (in-memory)."""
    if "optimisation_result" not in SESSION:
        return _err(409, "nothing to save — run a constrained optimise first")
    SESSION.setdefault("scenarios", {})[label] = {
        "optimisation_result": SESSION["optimisation_result"],
        "total_budget": SESSION.get("last_total_budget"),
    }
    return _ok({"saved": label, "all": list(SESSION["scenarios"])})


def compare_scenarios(labels: list[str]) -> dict[str, Any]:
    """Compare saved scenarios IN MEMORY (no endpoint). Revenue/ROAS/allocation deltas."""
    saved = SESSION.get("scenarios", {})
    missing = [x for x in labels if x not in saved]
    if missing:
        return _err(404, f"scenarios not saved: {missing}")
    out = {}
    for lbl in labels:
        rows = saved[lbl]["optimisation_result"]["dateRangeToResponseMap"]["aggregated_aggregated"]["mmmBudgetOptimisationResponseList"]
        ap = next((r for r in rows if r.get("platformName") == "All Platforms"), {})
        rev = (ap.get("optimisedBudgetData") or {}).get("response")
        bud = saved[lbl]["total_budget"]
        out[lbl] = {"total_budget": bud, "optimised_revenue": rev,
                    "roas": round(rev / bud, 2) if rev and bud else None}
    return _ok({"scenario_count": len(out), "scenarios": out})


def calculate_target_budget(target_revenue: float, current_revenue: float,
                            all_platform_revenue: float) -> dict[str, Any]:
    """Pure math (NO endpoint): required_budget = target - current + all_platform_revenue."""
    return _ok({"required_budget": float(target_revenue) - float(current_revenue) + float(all_platform_revenue),
                "formula": "target_revenue - current_revenue + all_platform_revenue"})


